public class Switchcase08 {
    public static void main(String[] args) {
        int num = 2000;
        switch (num) {
            case 200:System.out.println("Case 1");
            break;

            case 300:System.out.println("Case 2");
            break;

            case 100:System.out.println("case 3");
                break;
            
            default : System.out.println("default");
        }
    }
}
